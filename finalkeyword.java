public class finalkeyword {
    public static void main(String[] args) {
        int a = 4;
        System.out.println(a);
        a = 5;
        System.out.println(a);

        final int b = 4;
        System.out.println(b);
        // b = 5;
        // System.out.println(b); // show error
    }
}
